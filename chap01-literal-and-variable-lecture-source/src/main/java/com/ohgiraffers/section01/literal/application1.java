package com.ohgiraffers.section01.literal;

public class application1 {
    public static void main(String[] args){
        System.out.println(123);//정수 Integer
        System.out.println(1.23);//실수 Float
        System.out.println('a');//문자 Character
        System.out.println('1');//문자
        System.out.println("안녕하세요");//문자열 String
        System.out.println("123");//문자열
        System.out.println("");//문자열은 공백이여도 에러나지 않음
        System.out.println("a");
        System.out.println(true);//Boolean


    }
}
